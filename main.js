			let dat = new Date(elapsedTime);
			let mins = dat.getMinutes();
			let secs = dat.getSeconds();
			let mils = dat.getMilliseconds();

			let angleBetweenSecs = 360 / 60;
			let angle = (360 * mins) + (angleBetweenSecs * secs) + ((angleBetweenSecs / 1000) * mils);


